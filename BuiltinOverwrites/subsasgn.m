function out=subsasgn(in,index,val)  % meant to shadow the original version and perform a case if necessary
if isa(val,'cuda')
    out=builtin('subsasgn',in,index,single_force(val));  % force the cast
else
    out=builtin('subsasgn',in,index,val);  % use the normal version of whatever type
end

